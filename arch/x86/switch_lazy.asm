global perform_lazy_context_switch
perform_lazy_context_switch:
    push rax
    push rbx
    push rcx
    push rdx
    push rsi
    push rdi
    push rbp
    push r8
    push r9
    push r10
    push r11
    push r12
    push r13
    push r14
    push r15
    mov [rdi + 8], rsp
    mov rsp, [rsi + 8]
    mov rax, cr0
    or rax, 0x08
